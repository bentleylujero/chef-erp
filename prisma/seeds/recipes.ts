export async function seedRecipes(prisma: PrismaClient) {
  console.log("\n🍽️  Seeding recipes...\n");

  // 1. Build ingredient name → id lookup
  const allIngredients = await (prisma.ingredient as any).findMany({
    select: { id: true, name: true },
  });
  const ingredientMap = new Map<string, string>();
  for (const ing of allIngredients) {
    ingredientMap.set(ing.name, ing.id);
    ingredientMap.set(ing.name.toLowerCase(), ing.id);
  }

  let created = 0;
  let skipped = 0;
  const warnings: string[] = [];

  for (let idx = 0; idx < recipes.length; idx++) {
    const recipe = recipes[idx];
    if (idx % 10 === 0) console.log(`  Processing recipe ${idx + 1}/${recipes.length}...`);
    // Idempotency: skip if a system recipe with this exact title already exists
    const existing = await (prisma.recipe as any).findFirst({
      where: { title: recipe.title, ownerUserId: null },
    });
    if (existing) {
      skipped++;
      continue;
    }

    // Resolve ingredient names → IDs
    const resolvedIngredients: {
      ingredientId: string;
      quantity: number;
      unit: string;
      isOptional: boolean;
      prepNote: string | null;
    }[] = [];

    let missingIngredient = false;
    for (const ing of recipe.ingredients) {
      const id = ingredientMap.get(ing.name) ?? ingredientMap.get(ing.name.toLowerCase());
      if (!id) {
        warnings.push(`  ⚠️  "${recipe.title}": ingredient "${ing.name}" not found — skipping recipe`);
        missingIngredient = true;
        break;
      }
      resolvedIngredients.push({
        ingredientId: id,
        quantity: ing.quantity,
        unit: ing.unit,
        isOptional: ing.isOptional ?? false,
        prepNote: ing.prepNote ?? null,
      });
    }

    if (missingIngredient) continue;

    try {
      await (prisma.recipe as any).create({
        data: {
          title: recipe.title,
          description: recipe.description,
          cuisine: recipe.cuisine,
          difficulty: recipe.difficulty,
          techniques: recipe.techniques,
          instructions: recipe.instructions,
          prepTime: recipe.prepTime,
          cookTime: recipe.cookTime,
          servings: recipe.servings,
          flavorTags: recipe.flavorTags,
          tags: recipe.tags,
          source: "IMPORTED",
          ownerUserId: null,
          status: "active",
          ingredients: {
            create: resolvedIngredients,
          },
        },
      });
      created++;
    } catch (error: any) {
      warnings.push(`  ❌ "${recipe.title}": ${error.message}`);
    }
  }

  if (warnings.length > 0) {
    console.log(warnings.join("\n"));
  }

  console.log(
    `\n✅ Seeded ${created} recipes (${skipped} already existed, ${warnings.length} warnings)`,
  );
  console.log(`   Total recipe descriptors: ${recipes.length}`);
}
